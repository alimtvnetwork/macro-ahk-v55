#!/usr/bin/env node
// H8 — Extract every "- [ ]" / "- [x]" bullet from spec/2026-spec/01-prompt-spec/
// into one master acceptance checklist.
//
// Writes: /mnt/documents/2026-prompts-acceptance.md

import { readdirSync, readFileSync, statSync, mkdirSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";

const ROOT = "spec/2026-spec/01-prompt-spec";
const OUT  = "/mnt/documents/2026-prompts-acceptance.md";
const BULLET_RE = /^\s*-\s\[( |x|X)\]\s+(.+?)\s*$/;

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir).sort()) {
    const p = join(dir, name);
    const s = statSync(p);
    if (s.isDirectory()) out.push(...walk(p));
    else if (/\.md$/.test(name)) out.push(p);
  }
  return out;
}

const sections = [];
let total = 0, done = 0;

for (const file of walk(ROOT)) {
  const rel = relative(ROOT, file).replaceAll("\\", "/");
  const items = [];
  readFileSync(file, "utf8").split(/\r?\n/).forEach((line, i) => {
    const m = line.match(BULLET_RE);
    if (!m) return;
    total++;
    const checked = m[1].toLowerCase() === "x";
    if (checked) done++;
    items.push({ line: i + 1, checked, text: m[2] });
  });
  if (items.length) sections.push({ rel, items });
}

const lines = [
  "# 2026-spec — Master acceptance checklist",
  "",
  `Generated by \`scripts/extract-prompts-acceptance.mjs\`.`,
  `Total: **${total}**  ·  Checked: **${done}**  ·  Open: **${total - done}**`,
  "",
];
for (const s of sections) {
  lines.push(`## ${s.rel}`, "");
  for (const it of s.items) {
    lines.push(`- [${it.checked ? "x" : " "}] (L${it.line}) ${it.text}`);
  }
  lines.push("");
}

mkdirSync("/mnt/documents", { recursive: true });
writeFileSync(OUT, lines.join("\n"));
console.log(`✓ wrote ${OUT}  (${total} bullets across ${sections.length} files)`);
