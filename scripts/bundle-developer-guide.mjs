#!/usr/bin/env node
/**
 * bundle-developer-guide.mjs
 *
 * Reads all markdown files from spec/07-devtools-and-injection/developer-guide/
 * and generates a TypeScript module with their contents as string constants.
 *
 * Output: src/lib/developer-guide-data.generated.ts
 *
 * Run: node scripts/bundle-developer-guide.mjs
 */

import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from "node:fs";
import { join, resolve } from "node:path";

const ROOT = resolve(import.meta.dirname, "..");
const GUIDE_DIR = join(ROOT, "spec", "12-devtools-and-injection", "developer-guide");
const OUT_PATH = join(ROOT, "src", "lib", "developer-guide-data.generated.ts");

function main() {
    if (!existsSync(GUIDE_DIR)) {
        console.error("❌ Developer guide directory not found:", GUIDE_DIR);
        process.exit(1);
    }

    const files = readdirSync(GUIDE_DIR)
        .filter(f => f.endsWith(".md"))
        .sort();

    if (files.length === 0) {
        console.warn("⚠ No .md files found in developer-guide/");
        return;
    }

    const sections = files.map(f => {
        const content = readFileSync(join(GUIDE_DIR, f), "utf-8");
        return { name: f, content };
    });

    const lines = [
        "/**",
        " * Developer Guide Data — AUTO-GENERATED",
        " *",
        " * Generated by: scripts/bundle-developer-guide.mjs",
        ` * Source: spec/07-devtools-and-injection/developer-guide/ (${sections.length} files)`,
        ` * Generated: ${new Date().toISOString()}`,
        " *",
        " * Do NOT edit manually. Re-run the script to update.",
        " */",
        "",
        `export const GUIDE_SECTION_COUNT = ${sections.length};`,
        "",
        "export const GUIDE_SECTIONS: readonly string[] = [",
    ];

    for (const section of sections) {
        // Escape backticks and ${} in template literals
        const escaped = section.content
            .replace(/\\/g, "\\\\")
            .replace(/`/g, "\\`")
            .replace(/\$\{/g, "\\${");
        lines.push(`  // ${section.name}`);
        lines.push(`  \`${escaped}\`,`);
        lines.push("");
    }

    lines.push("] as const;");
    lines.push("");

    mkdirSync(resolve(OUT_PATH, ".."), { recursive: true });
    writeFileSync(OUT_PATH, lines.join("\n"), "utf-8");
    console.log(`✅ Bundled ${sections.length} guide docs → ${OUT_PATH}`);
}

main();
