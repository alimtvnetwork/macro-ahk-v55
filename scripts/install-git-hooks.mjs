#!/usr/bin/env node
/**
 * install-git-hooks.mjs — Idempotent native git-hook installer
 *
 * Installs `.git/hooks/pre-commit` to invoke
 * `node scripts/lint-staged-standalone.mjs`. Zero new dependencies
 * (no husky, no lint-staged) — matches the project's existing
 * "vanilla node + git" tooling philosophy.
 *
 * Run automatically by the npm `prepare` lifecycle so every fresh
 * `pnpm install` re-installs the hook on contributor machines.
 * Safe to re-run: rewrites the hook file every time, only if its
 * contents differ from the canonical body.
 *
 * Skips silently when:
 *   - .git/ is missing (npm install in a tarball / CI without git)
 *   - SKIP_GIT_HOOKS=1 is set in env (CI override)
 *
 * Author: Riseup Asia LLC
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(SCRIPT_DIR, "..");
const GIT_PATH = path.join(REPO_ROOT, ".git");

if (process.env.SKIP_GIT_HOOKS === "1") {
    console.log("[install-git-hooks] skip — SKIP_GIT_HOOKS=1");
    process.exit(0);
}
if (!fs.existsSync(GIT_PATH)) {
    console.log(`[install-git-hooks] skip — no .git at ${GIT_PATH} (not a git checkout)`);
    process.exit(0);
}

/**
 * Resolve the real hooks directory.
 *   - .git is a directory                → <repo>/.git/hooks
 *   - .git is a file ("gitdir: <path>")  → linked worktree; hooks live at <path>/hooks
 *
 * Returns null when we cannot resolve a writable hooks dir (e.g. sandbox
 * worktree pointing at a read-only Nix store, as in Lovable's preview env).
 */
function resolveHookDir() {
    const stat = fs.statSync(GIT_PATH);
    if (stat.isDirectory()) return path.join(GIT_PATH, "hooks");
    if (!stat.isFile()) {
        console.warn(`[install-git-hooks] WARN — .git at ${GIT_PATH} is neither file nor directory; skipping.`);
        return null;
    }
    const contents = fs.readFileSync(GIT_PATH, "utf8").trim();
    const match = /^gitdir:\s*(.+)$/m.exec(contents);
    if (!match) {
        console.warn(`[install-git-hooks] WARN — .git file at ${GIT_PATH} has no 'gitdir:' line; skipping.`);
        return null;
    }
    const gitdir = path.isAbsolute(match[1]) ? match[1] : path.resolve(REPO_ROOT, match[1]);
    const hooksDir = path.join(gitdir, "hooks");
    // Probe writability — read-only stores (Nix sandbox) silently skip.
    try {
        fs.mkdirSync(hooksDir, { recursive: true });
        fs.accessSync(hooksDir, fs.constants.W_OK);
    } catch (err) {
        console.log(`[install-git-hooks] skip — hooks dir not writable at ${hooksDir} (${err instanceof Error ? err.code ?? err.message : String(err)}). This is normal in read-only sandboxes.`);
        return null;
    }
    return hooksDir;
}

const HOOK_DIR = resolveHookDir();
if (!HOOK_DIR) process.exit(0);
const HOOK_PATH = path.join(HOOK_DIR, "pre-commit");

const HOOK_BODY = `#!/usr/bin/env sh
# AUTO-GENERATED by scripts/install-git-hooks.mjs — do not edit by hand.
# Re-run \`pnpm install\` (or \`node scripts/install-git-hooks.mjs\`) to refresh.
#
# Runs ESLint with --max-warnings=0 against staged standalone-scripts TS/TSX
# files. Catches issues like sonarjs/no-nested-template-literals before they
# reach CI.
#
# Bypass (audited in git log):
#   git commit --no-verify

set -e

# Resolve repo root from this hook's location (.git/hooks/pre-commit).
HOOK_DIR=$(dirname "$0")
REPO_ROOT=$(cd "$HOOK_DIR/../.." && pwd)

exec node "$REPO_ROOT/scripts/lint-staged-standalone.mjs"
`;

if (!fs.existsSync(HOOK_DIR)) fs.mkdirSync(HOOK_DIR, { recursive: true });

let needsWrite = true;
if (fs.existsSync(HOOK_PATH)) {
    const existing = fs.readFileSync(HOOK_PATH, "utf8");
    if (existing === HOOK_BODY) needsWrite = false;
}

if (needsWrite) {
    fs.writeFileSync(HOOK_PATH, HOOK_BODY, { mode: 0o755 });
    console.log(`[install-git-hooks] installed ${path.relative(REPO_ROOT, HOOK_PATH)}`);
} else {
    console.log(`[install-git-hooks] up-to-date ${path.relative(REPO_ROOT, HOOK_PATH)}`);
}

// Belt-and-braces: ensure executable bit on every run (in case of git/copy strip).
try {
    fs.chmodSync(HOOK_PATH, 0o755);
} catch (err) {
    console.warn(`[install-git-hooks] WARN — could not chmod ${HOOK_PATH}: ${err instanceof Error ? err.message : String(err)}`);
}

process.exit(0);
